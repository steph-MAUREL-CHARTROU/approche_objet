package fr.entites;

public class AdressePostale {
	
	
	public int numRue;
	public String rue;
	public String codePostal;
	public String ville;
	
	
	public AdressePostale(int numRue, String rue, String codePostal, String ville) {
		
		this.numRue = numRue;
		this.rue = rue;
		this.codePostal = codePostal;
		this.ville = ville;
	}


}
